local KeybindRegistry = {}
local isBindingActive = false
local activeBindingBtn = nil
local activeRouteToBind = nil

local function registerRouteKeybind(routeName, keyCode, callback)
    KeybindRegistry[routeName] = {Key = keyCode, CB = callback}
end

-- Listens to global physical key presses to trigger custom route keybinds
local KeybindConnection = UserInputService.InputBegan:Connect(function(input, processed)
    if processed then return end
    
    -- Handle interactive binding state
    if isBindingActive and activeBindingBtn and activeRouteToBind then
        if input.KeyCode ~= Enum.KeyCode.Unknown and input.KeyCode ~= Enum.KeyCode.None then
            local pressedKey = input.KeyCode
            isBindingActive = false
            
            -- Save keybind to registry
            if KeybindRegistry[activeRouteToBind] then
                KeybindRegistry[activeRouteToBind].Key = pressedKey
            end
            
            activeBindingBtn.Text = "[ " .. pressedKey.Name .. " ]"
            activeBindingBtn = nil
            activeRouteToBind = nil
            return
        end
    end
    
    -- Execute standard trigger when keybind matches
    for routeName, bindInfo in pairs(KeybindRegistry) do
        if bindInfo.Key ~= Enum.KeyCode.None and input.KeyCode == bindInfo.Key then
            pcall(bindInfo.CB)
        end
    end
end)
table.insert(_G.LouisConnections, KeybindConnection)

